<a href="#srgintro"><h2>Introductory Talk on the topic of Control Systems and Control Theory</h2></a>
<h4>Title:    Control means PID, Seriously? Times change, so do controls</h4>
<h4>Date:     Wednesday, 13th April</h4>
<h4>Abstract:</h4>
When one says word control, second word that comes to anyone's mind is PID.
In todays time, when computational power has sky-folded, there are other controls in use like nonlinear control, adaptive control, robust control and so on.
One of the fastest growing control is Model Predictive Control, where in it makes use of optimization to deliver the desired objective in presence of constraints.

Talk will start from basics of control and go on to What, How, Why w.r.t. MPC?
A worth attend for those who work in the area of controls.
A recommended talk for those who think controls is not relevant to them.
